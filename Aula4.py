# Tipo int e Float
# int -> Números inteiros
# int representa qualquer número, negativo ou positivo
# Int sem sinal é considerado positivo
print(11) #int
print(-11) #int
print(0) #int

# Floar -> Número com ponto flutuante
# O tipo float representa qualquer número, negativo ou positivo com ponto flutuante
# Número float sem sinal é considerado número positivo
print(1.1, 10.11)
print(0.0, -1.5)

#A função type mostra o tipo que o python inferiu o valor
print( type("Gabriel") )
print( type(1) )
print( type(-10) )
print( type(0) )
print( type(-9.0) )
print( type(0.0) )
